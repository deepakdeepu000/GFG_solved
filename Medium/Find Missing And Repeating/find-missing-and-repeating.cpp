class Solution{
public:
    vector<int> findTwoElement(vector<int> arr, int n) {
        // code here
        sort(arr.begin(),arr.end());
        int cnt = 1,res = 0;
        for(int i = 0;i < n;i++){
            if(arr[i] == cnt ) cnt++;
            else if(arr[i-1] == arr[i]) res = arr[i];
        }
        
        return {res,cnt};
    }
};

